// Ensure both languages have the same keys
export type Translations = typeof translations.en

interface LanguageContextType {
  lang: Language
  setLang: (lang: Language) => void
  t: (key: TranslationKey) => string
  getGuestName: (name: string, nameHindi?: string) => string
}

const LanguageContext = createContext<LanguageContextType>({
  lang: 'en',
  setLang: () => {},
  t: (key) => translations.en[key],
  getGuestName: (name) => name,
})

export function LanguageProvider({ children }: { children: ReactNode }) {
  const [lang, setLangState] = useState<Language>(() => {
    if (typeof window !== 'undefined') {
      const saved = localStorage.getItem('app_language') as Language | null
      if (saved && (saved === 'en' || saved === 'hi')) {
        return saved
      }
    }
    return 'en'
  })

  const setLang = (newLang: Language) => {
    setLangState(newLang)
    localStorage.setItem('app_language', newLang)
  }

  const t = (key: TranslationKey): string => {
    return translations[lang][key] || translations.en[key] || key
  }

  return (
    <LanguageContext.Provider value={{ lang, setLang, t, getGuestName: (name: string, nameHindi?: string) => lang === 'hi' && nameHindi && nameHindi.trim() !== '' ? nameHindi : name }}>
      {children}
    </LanguageContext.Provider>
  )
}

export function useLanguage() {
  return useContext(LanguageContext)
}

export function getGuestName(lang: Language, name: string, nameHindi?: string): string {
  return lang === 'hi' && nameHindi && nameHindi.trim() !== '' ? nameHindi : name
}

export type { TranslationKey }
